const languageselector = document.querySelector('select');

languageselector.addEventListener('change', (event) => {
  setlangauge(event.target.value);
  localStorage.setItem('lang', event.target.value);
});
document.addEventListener('DOMContentLoaded', () => {
    const langauge = localStorage.getItem('lang');
    setlangauge(langauge);
});
const setlangauge = (langauge) => {
    const element =document.querySelectorAll('[data-tra]');
    element.forEach((element) => {
        const key = element.getAttribute('data-tra');
        element.textContent = translation[langauge][key];
    });
    document.dir = langauge === 'ar' ? 'rtl' : 'ltr';
}

